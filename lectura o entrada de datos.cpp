//Lectura o entrada de datos

#include<iostream>

using namespace std;

int main(){
	
	int numero; //declaración de variable
	cout<<"Escriba un numero: ";
	cin>>numero; //comando de entrada de datos
	
	cout<<"\nEl numero que escribio es: "<<numero<<endl; //muestra resultado con salto de lína
	
	float decimal; //declaración de variable
	cout<<"Escriba un numero decimal: ";
	cin>>decimal; //comando de entrada de datos
	
	cout<<"\nEl decimal introducido es: "<<decimal<<endl; //muestra el resultado con salto de línea
	
	return 0;
}
